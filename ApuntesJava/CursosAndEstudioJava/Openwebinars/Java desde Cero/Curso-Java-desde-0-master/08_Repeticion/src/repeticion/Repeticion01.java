/**
 *  Ejemplo de uso del bucle while
 */
package repeticion;

/**
 * @author Openwebinars
 *
 */
public class Repeticion01 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int contador = 1;
        while (contador < 11) {
            System.out.println("Contador vale: " + contador);
            contador++;
        }

	}

}
