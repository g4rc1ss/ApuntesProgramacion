/**
 * Ejemplo de uso del bucle do-while
 */
package repeticion;

/**
 * @author Openwebinars
 *
 */
public class Repeticion02 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int contador = 1;
        do {
            System.out.println("Contador vale: " + contador);
            contador++;
        } while (contador < 11);

	}

}
